const { InternalServerError } = require("infra/errors");

const availableFeatures = [
  //user
  "create:user",
  "read:user",
  "read:user:self",
  "update:user",
  "update:user:others",

  //session
  "create:session",
  "read:session",

  // activation token
  "read:activation_token",

  //migration
  "create:migration",
  "read:migration",

  //status
  "read:status",
  "read:status:all",
];

function can(user, feature, resource) {
  validateUser(user);
  validateFeature(feature);

  let authorized = false;

  if (user.features.includes(feature)) {
    authorized = true;
  }

  if (feature === "update:user" && resource) {
    authorized = false;

    if (user.id === resource.id || can(user, "update:user:others")) {
      authorized = true;
    }
  }

  if (feature === "read:status:admin") {
    if (user.features.includes("read:status:admin")) {
      authorized = true;
    }
  }

  return authorized;
}

function filterOutput(user, feature, resource) {
  validateUser(user);
  validateFeature(feature);
  validateResource(resource);

  if (feature === "read:user") {
    return {
      id: resource.id,
      username: resource.username,
      features: resource.features,
      created_at: resource.created_at,
      updated_at: resource.updated_at,
    };
  }

  if (feature === "read:user:self") {
    if (user.id === resource.id)
      return {
        id: resource.id,
        username: resource.username,
        features: resource.features,
        email: resource.email,
        created_at: resource.created_at,
        updated_at: resource.updated_at,
      };
  }

  if (feature === "read:session") {
    if (user.id === resource.user_id)
      return {
        id: resource.id,
        token: resource.token,
        user_id: resource.user_id,
        created_at: resource.created_at,
        updated_at: resource.updated_at,
        expires_at: resource.expires_at,
      };
  }

  if (feature === "read:activation_token") {
    return {
      id: resource.id,
      user_id: resource.user_id,
      created_at: resource.created_at,
      updated_at: resource.updated_at,
      expires_at: resource.expires_at,
      used_at: resource.used_at,
    };
  }

  if (feature === "read:migration") {
    return resource.map((migration) => ({
      path: migration.path,
      name: migration.name,
      timestamp: migration.timestamp,
    }));
  }

  if (feature === "read:status") {
    return {
      updated_at: resource.updated_at,
      dependencies: {
        database: {
          version: can(user, "read:status:all")
            ? resource.dependencies.database.version
            : undefined,
          max_connections: resource.dependencies.database.max_connections,
          opened_connections: resource.dependencies.database.opened_connections,
        },
      },
    };
  }
}

function validateUser(user) {
  if (!user || !user.features) {
    throw new InternalServerError({
      cause: "É necessário fornecer o `user` no model authorization",
    });
  }
}

function validateFeature(feature) {
  if (!feature || !availableFeatures.includes(feature)) {
    throw new InternalServerError({
      cause: `O recurso ${feature} não é um recurso conhecido`,
    });
  }
}

function validateResource(resource) {
  if (!resource) {
    throw new InternalServerError({
      cause: "É necessário fornecer o `resource` no model authorization",
    });
  }
}

const authorization = {
  can,
  filterOutput,
};

export default authorization;
